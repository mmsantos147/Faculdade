package org.example;

public class Calculator {
    public int add(int a, int b){
        return a + b;
    }

    public int sub (int a, int b) {
        return a - b;
    }

    public int mult (int a, int b) {
        return a * b;
    }
    public float divide(int a, int b) {
        return (float) a / b;
    }
}
